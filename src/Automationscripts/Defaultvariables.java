package Automationscripts;

public class Defaultvariables {
	static byte bytevariable;
	static short variable;
	static int value;
	static long type;
	static float number;
	static double no;
	static boolean statement;
	static char pavan;
	
	public static void main(String[] args)
	{
		
		System.out.println ("Byte Value:" + bytevariable);
		System.out.println("Short Value:" + variable);
		System.out.println("int Value:" + value);
		System.out.println("Long Value:" + type);
		System.out.println("Float Value:" + number);
		System.out.println("Double value:" + no);
		System.out.println("Boolean value:" + statement);
		System.out.println("Char Value:"+ pavan);
			
	}

}
