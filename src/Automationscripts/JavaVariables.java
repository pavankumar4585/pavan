package Automationscripts;

public class JavaVariables {
	
	public static void main(String[] args) {
		
		
	/*byte
	 * min: -128
	 * max: 127
	 * default:0
	 * 
	 */
	byte bytevariable=100;
	System.out.println ("Byte Value:" + bytevariable);
	
	/*
	 * Short
	 * min: -32768
	 * max: 32767
	 * default: 0
	 */
	
	short variable=32760;
	System.out.println("Short Value:" + variable);
	
	/*
	 * int
	 * min: -2,147,483,648
	 * max: 2,147,483,647
	 * default: 0
	 */
	
	int value=2147483646;
	System.out.println("int Value:" + value);
	
	/*
	 * long
	 * min: -2^63
	 * max: 2^63 -1
	 * default: 0
	 */
	
	long type=55555555L;
	System.out.println("Long Value:" + type);
	
	/*
	 * float
	 * default:0.0f
	 */
	float number=52.36f;
	System.out.println("Float Value:" + number);
	
	/*
	 * double
	 * default: 0.0d
	 */
	double no=5.23;
	System.out.println("Double value:" + no);
	
	/*
	 * boolean
	 * false and true
	 * default:false
	 */
	
	boolean statement=true;
	System.out.println("Boolean value:" + statement);
	
	/*
	 * Char
	 * min: 0
	 * max: 65,535
	 */
	
	char pavan= 'a';
	System.out.println("Char Value:"+ pavan);
	
	
	}

	
}
